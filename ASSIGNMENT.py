# Q6
print("GCD of 36 and 60:", math.gcd(36, 60))

# STATISTICAL MODULE EXERCISE:

import statistics

# Q1
data1 = [10, 20, 30, 40, 50]
print("Mean:", statistics.mean(data1))

# Q2
data2 = [1, 2, 3, 4, 5, 6]
print("Median:", statistics.median(data2))

# Q3
data3 = [2, 4, 4, 6, 8, 8, 8, 10]
print("Mode:", statistics.mode(data3))

# Q4
data4 = [5, 10, 15, 20, 25]
print("Variance:", statistics.variance(data4))

# Q5
data5 = [10, 20, 30, 40, 50]
print("Standard Deviation:", statistics.stdev(data5))

# Q6
data6 = [10, 20, 30, 40]
print("Low Median:", statistics.median_low(data6))

# Q7
print("High Median:", statistics.median_high(data6))



