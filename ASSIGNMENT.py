# MATH MODULE EXCERCISE
import math

# Q1
print("Square Root of 144:", math.sqrt(144))

# Q2
print("5 Power 3:", math.pow(5, 3))

# Q3
print("Floor of 7.9:", math.floor(7.9))

# Q4
print("Ceil of 7.1:", math.ceil(7.1))

# Q5
